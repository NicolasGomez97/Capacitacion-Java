public class TipoPrimitivoEntero {
    public static void main(String[] args) {
        short nshort = 32767;
        System.out.println("nbyte = " + nshort);
        System.out.println("Valor minimo: " + Short.MIN_VALUE +  "\nValor Maximo: " + Short.MAX_VALUE);

        int nint = 2147483647;
        System.out.println("nint = " + nint);
        System.out.println("Valor minimo: " + Integer.MIN_VALUE +  "\nValor Maximo: " + Integer.MAX_VALUE);

        long nlong =   9223372036854775807L;
        System.out.println("nlong = " + nlong);
        System.out.println("Valor minimo: " + Long.MIN_VALUE +  "\nValor Maximo: " + Long.MAX_VALUE);

    }
    }
