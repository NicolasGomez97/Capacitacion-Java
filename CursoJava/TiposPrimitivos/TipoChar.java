public class TipoChar {
    public static void main(String[] args) {
        char miCaracter = 'a';
        System.out.println("miCaracter = " + miCaracter);

        char varChar = '\u0021'; // Unicode
        System.out.println("varChar = " + varChar);

        char varCharDecimal = 33; // Decimal
        System.out.println("varCharDecimal = " + varCharDecimal);

        char varCharSimbolo =  '!';
        System.out.println("varCharSimbolo = " + varCharSimbolo);

        var varChar1 = '\u0021'; // Unicode
        System.out.println("varChar1 = " + varChar1);

        var varCharDecimal1 = 33; // Decimal
        System.out.println("varCharDecimal1 = " + varCharDecimal1);

        var varCharSimbolo1 =  '!';
        System.out.println("varCharSimbolo1 = " + varCharSimbolo1);
    }
}
