public class TiposPrimitivosFlotante {
    public static void main(String[] args) {
        /*
            Tipos primitivos de tipo flotantes: float y double
         */
        float numeroFloat =  3.4028235E38F;
        System.out.println("numeroFloat = " + numeroFloat);
        System.out.println("Numero Maximo: " + Float.MAX_VALUE);
        System.out.println("Numero Minimo: "+ Float.MIN_VALUE);

        double numeroDouble = 1.7976931348623157E308D;
        System.out.println("numeroDouble = " + numeroDouble);
        System.out.println("Numero Maximo :"+ Double.MAX_VALUE);
        System.out.println("Numero Minimo: "+ Double.MIN_VALUE);

    }
}
