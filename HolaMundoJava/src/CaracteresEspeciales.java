public class CaracteresEspeciales {
    public static void  main(String args[]){
        var nombre = "Nicolas";
        // Salto de Linea \n
        System.out.println("Nueva liena: \n" + nombre);
        // Tabulador \t
        System.out.println("Tabulador: \t" + nombre);
        // Retoceso: \b
        System.out.println("Retoceso: \b" + nombre);
        // Comilla simple \'
        System.out.println("Comilla simple: \'" + nombre + "\'");// En este caso no es necesario
        // Comilla doble \"
        System.out.println("Comilla doble: \"" + nombre +"\"");


    }
}
